  }
  
  /// Convert regular UI text to spiritual equivalent
  static String spiritualize(String regularText) {
    final mappings = {
      'login': login,
      'logout': logout,
      'search': search,
      'home': home,
      'favorites': favorites,
      'profile': profile,
      'settings': settings,
      'loading': loading,
      'read more': readMore,
      'view': view,
      'share': share,
      'save': save,
    };
    
    return mappings[regularText.toLowerCase()] ?? regularText;
  }
