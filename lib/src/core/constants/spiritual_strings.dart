  /// Get random loading message
  static String get randomLoadingMessage {
    final messages = List<String>.from(loadingMessages);
    messages.shuffle();
    return messages.first;
  }
  
  /// Get spiritual greeting based on time of day
  static String get spiritualGreeting {
    final hour = DateTime.now().hour;
    if (hour < 12) {
      return "Morning Mercies Upon You";
    } else if (hour < 17) {
      return "Midday Blessings";
    } else {
      return "Evening Grace Upon You";
    }
  }
  
